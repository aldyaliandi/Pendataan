    private void setTablependataan(List<pendataan> listpendataan) {
        if (listpendataan==null){
            JOptionPane.showMessageDialog(this, "Data "
                    + "data_mhs gagal diambil dari database","DATABASE FAILED", JOptionPane.ERROR_MESSAGE);
        }else{
            DefaultTableModel tableModel = new DefaultTableModel();
            tableModel.setColumnIdentifiers(
                    new String[] {
                        "nama",
                        "nrp",
                        "kelas",
                        "email",
                        "sudah_daftar"
                    }
            );
            
            for (pendataan g : listpendataan) {
                Object[] o = new Object[5];
                o[0] = g.getNama();
                o[1] = g.getNrp();
                o[2] = g.getKelas();
                o[3] = g.getEmail();
                o[5] = g.getSudah_daftar();
                tableModel.addRow(o);
            }
            tblpendataan.setAutoResizeMode( javax.swing.JTable.AUTO_RESIZE_ALL_COLUMNS);
            tblpendataan.setModel(tableModel);
            }
        
        }


