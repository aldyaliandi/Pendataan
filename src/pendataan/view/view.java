    private void tblpendataanMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblpendataanMousePressed
        if(evt.getClickCount() == 2){
            int idxrow = tblpendataan.getSelectedRow();
            txtnama.setText(tblpendataan.getModel().getValueAt(idxrow, 0).toString());
            txtnrp.setText(tblpendataan.getModel().getValueAt(idxrow, 1).toString());
            txtkelas.setText(tblpendataan.getModel().getValueAt(idxrow, 2).toString());
            txtemail.setText(tblpendataan.getModel().getValueAt(idxrow, 3).toString());
            txtIdpendataan.setText(tblpendataan.getModel().getValueAt(idxrow, 4).toString());
            btnsimpan.setText("Update");
        }
         if(evt.isPopupTrigger()){
        popuppendataan.show(tblpendataan, evt.getX(), evt.getY());
         }
    }//GEN-LAST:event_tblpendataanMousePressed

